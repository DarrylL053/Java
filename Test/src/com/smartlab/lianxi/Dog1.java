package com.smartlab.lianxi;

public class Dog1 {
	private String name;
	private String color;
	private int age;
	public Dog1(String name , String color , int age)
	{
		this.age = age;
		this.color = color;
		this.name = name;
	}
	public String getName() {
		return name;
	}
	public String getColor() {
		return color;
	}
	public int getAge() {
		return age;
	}
	

}
